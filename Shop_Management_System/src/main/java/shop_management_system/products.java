    private void AddButtoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtoActionPerformed
            if(txtproductId.getText().equals("")||txtName.getText().equals("")||txtQuantity.getText().equals("")||txtPrice.getText().equals("")||jComboBox1.getSelectedItem().equals("")){
                JOptionPane.showMessageDialog(null, "Invalid Login Details","Login Error",JOptionPane.ERROR_MESSAGE);
            }
            else{
                String[] data = {txtproductId.getText(),txtName.getText(),txtQuantity.getText(),txtPrice.getText(),jComboBox1.getSelectedItem().toString()};
                DefaultTableModel table_model = (DefaultTableModel)listTable.getModel();
                table_model.addRow(data);
                JOptionPane.showMessageDialog(this, "Data added Successfully");
                
                try {
                String filepath = "E:\\Shop_Management_System\\Shop.txt";
                File file = new File(filepath);
                 if(!file.exists()){
                        file.createNewFile();
                 }
                 FileWriter writer = new FileWriter(file.getAbsoluteFile());
                 BufferedWriter bufferwriter = new BufferedWriter(writer);
                 for(int i =0; i < listTable.getRowCount(); i++ ){
                     for(int j = 0; j < listTable.getColumnCount(); j++){
                         bufferwriter.write(listTable.getValueAt(i, j).toString()+ " ");
                     }
                     bufferwriter.newLine();
                 }
                 bufferwriter.close();
                 writer.close();
                txtproductId.setText(null);
                txtName.setText(null);
                txtQuantity.setText(null);
                txtPrice.setText(null);
                jComboBox1.setSelectedIndex(0);
                } catch (IOException ex) {
                    Logger.getLogger(products.class.getName()).log(Level.SEVERE, null, ex);
                }       
            }
            
            
    }//GEN-LAST:event_AddButtoActionPerformed

