    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        
        try {
            DefaultTableModel table_model = (DefaultTableModel)listTable.getModel();
            if(listTable.getSelectedRowCount()==1){
                table_model.removeRow(listTable.getSelectedRow());
            }
            else{
                if(listTable.getRowCount()==0){
                    JOptionPane.showMessageDialog(this, "Table is Empty");
                }
                else{
                    JOptionPane.showMessageDialog(this, "Please Select Single Row For Delete.");
                }
            }  String filepath = "E:\\Shop_Management_System\\Shop.txt";
            File file = new File(filepath);
            FileWriter writer = new FileWriter(file.getAbsoluteFile());
            BufferedWriter bufferwriter = new BufferedWriter(writer);
            for(int i =0; i < listTable.getRowCount(); i++ ){
                for(int j = 0; j < listTable.getColumnCount(); j++){
                    bufferwriter.write(listTable.getValueAt(i, j).toString()+ " ");
                }
                bufferwriter.newLine();
            }
            bufferwriter.close();
            writer.close();
        } catch (IOException ex) {
            Logger.getLogger(products.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void AddButto1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButto1ActionPerformed
        String filepath = "E:\\Shop_Management_System\\Shop.txt";
        File file = new File(filepath);
        try {
            FileReader reader = new FileReader(file);
            BufferedReader buffereader = new BufferedReader(reader);
            DefaultTableModel table_model = (DefaultTableModel)listTable.getModel();
            Object[] lines = buffereader.lines().toArray();
            for(int i=0; i < lines.length;i++){
                String[] row = lines[i].toString().split(" ");
                table_model.addRow(row);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(products.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_AddButto1ActionPerformed

    private void listTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listTableMouseClicked
        DefaultTableModel table_model = (DefaultTableModel)listTable.getModel();      
        String ID = table_model.getValueAt(listTable.getSelectedRow(), 0).toString();
        String Name = table_model.getValueAt(listTable.getSelectedRow(), 1).toString();       
        String Quantity = table_model.getValueAt(listTable.getSelectedRow(), 2).toString();
        String Price = table_model.getValueAt(listTable.getSelectedRow(), 3).toString();
        String Category = table_model.getValueAt(listTable.getSelectedRow(), 4).toString();
        
        txtproductId.setText(ID);
        txtName.setText(Name);
        txtQuantity.setText(Quantity);
        txtPrice.setText(Price);
        jComboBox1.setSelectedItem(Category);
    }//GEN-LAST:event_listTableMouseClicked

    private void EditButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditButtonActionPerformed
        DefaultTableModel table_model = (DefaultTableModel)listTable.getModel();
        if(listTable.getSelectedRowCount()==1){
            String ID = txtproductId.getText();
            String name = txtName.getText();
            String quantity = txtQuantity.getText();
            String price = txtPrice.getText();
            String category = (String) jComboBox1.getSelectedItem();            
            table_model.setValueAt(ID,listTable.getSelectedRow(), 0);
            table_model.setValueAt(name,listTable.getSelectedRow(), 1);            
            table_model.setValueAt(quantity,listTable.getSelectedRow(), 2);
            table_model.setValueAt(price,listTable.getSelectedRow(), 3);
            table_model.setValueAt(category,listTable.getSelectedRow(), 4);
            
            JOptionPane.showMessageDialog(this, "Successfully Edited");
        }
        else{
            if(listTable.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Table is Empty");
            }
            else{
                JOptionPane.showMessageDialog(this, "Please select a single Row");
            }
        }
    }//GEN-LAST:event_EditButtonActionPerformed

